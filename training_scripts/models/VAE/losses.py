class VGGPerceptualLoss_Galaxy(torch.nn.Module):
    def __init__(self, device='cuda'):
        super().__init__()
        model = torchvision.models.vgg16(VGG16_Weights.IMAGENET1K_FEATURES).features
        blocks = []
        blocks.append(model[:4].eval())
        blocks.append(model[4:9].eval())
        blocks.append(model[9:16].eval())
        blocks.append(model[16:23].eval())
        for bl in blocks:
            for p in bl.parameters():
                p.requires_grad = False
        self.blocks = torch.nn.ModuleList(blocks)
    def forward(self, input, target, feature_layers=(0, 1, 2, 3), style_layers=()):
        if input.shape[1] != 3:
            input = input.repeat(1, 3, 1, 1)
            target = target.repeat(1, 3, 1, 1)
        loss = 0.0
        x = input
        y = target
        for i, block in enumerate(self.blocks):
            x = block(x)
            y = block(y)
            # b,c,h,w = x.shape
            if i in feature_layers:
                # loss += torch.nn.functional.l1_loss(x, y)
                loss += torch.nn.functional.mse_loss(x, y)
            if i in style_layers:
                act_x = x.reshape(x.shape[0], x.shape[1], -1)
                act_y = y.reshape(y.shape[0], y.shape[1], -1)
                _,c,h,w = x.shape
                gram_x = (act_x @ act_x.permute(0, 2, 1)) / (c*h*w)
                gram_y = (act_y @ act_y.permute(0, 2, 1)) / (c*h*w)
                loss += torch.nn.functional.l1_loss(gram_x, gram_y)
        return loss / (len(feature_layers) + len(style_layers))

